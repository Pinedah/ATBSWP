# Math Operators from Highest to Lowest Precedence

print(f"------------------\nOperator: **\nExponent \n2 ** 3 = {2**3}")

print(f"------------------\nOperator: %\nModulus/remainder \n22 % 8 = {22 % 8}")

print(f"------------------\nOperator: //\nInteger division \n22 // 8 = {22 // 8}")

print(f"------------------\nOperator: /\nDivision \n22 / 8 = {22 / 8}")

print(f"------------------\nOperator: *\nMultiplication \n11 * 8 = {11 * 8}")

print(f"------------------\nOperator: -\nSubtraction \n22 - 8 = {22 - 8}")

print(f"------------------\nOperator: /\nAddition \n22 + 8 = {22 + 8}")

